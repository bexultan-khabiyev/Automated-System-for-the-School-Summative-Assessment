      <h2>Description of the organization</h2>
      <p>
        Nazarbayev Intellectual Schools is a network of state educational schools, where favorable conditions are created for the development of creative potential and successful self-realization and integration of young citizens of Kazakhstan into the society. The project for the creation of state Intellectual Schools was launched in 2008 at the initiative of the First President of the Republic of Kazakhstan, Leader of the Nation Nursultan Nazarbayev. Intellectual schools are designed to become an experimental platform for the development, observation, study, research, analysis, implementation and realization of innovative teaching methods of modern educational programs. Since January 19, 2011, after the adoption of the Law of the Republic of Kazakhstan "On the Status of Nazarbayev University", "Nazarbayev Intellectual Schools" and "Nazarbayev Fund", Nazarbayev Intellectual Schools have a special legal status, which provides the right to independently approve educational curricula, establish requirements for entrance examinations, current control of progress, intermediate and final certification and more. Enrollment in the number of students of the Intellectual Schools is carried out in the following order: in grades 1-6 - on the basis of the protocol of the Competition Commission, the application of the applicant's legal representative and the order of the director of the Intellectual School; in grades 7-9, 11 - on the basis of the protocol of the Republican Commission for awarding the grant, the application of the applicant's legal representative and the order of the director of the Intellectual School.
