      <h2>Description of the current system</h2>
      <p>Schools operates on the basis of legal acts concerning:</p>
      <p>1) training programs;</p>             
      <p>2) criteria-based assessment;</p>
      <p>3) internal regulations for students;</p>
      <p>4) compliance with academic integrity;</p>
      <p>5) the grounds for expulsion from school;</p>
      <p>6) living in a boarding school.</p>
      <p> Criteria-based assessment, in turn, includes, among other things, internal summative assessment, which consists of speaking, listening, writing and reading skills. 
        In the process of assessing listening and speaking skills, the teacher must provide students with audio material for listening and questions to it, after which, on the spot, evaluate the speech of each student in turn. Currently, during distance learning, these skills are assessed in writing.
        In the process of assessing writing and reading skills, the teacher must provide students with a topic for writing, text and accompanying questions to it, then, evaluate the work of students in accordance with the established criteria.
